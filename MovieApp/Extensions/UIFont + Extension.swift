import UIKit

extension UIFont {
    static func jakarta32() -> UIFont? {
        return UIFont(name: "PlusJakartaSans-Bold", size: 32)
    }

    static func jakarta24() -> UIFont? {
        return UIFont(name: "PlusJakartaSans-Regular", size: 24)
    }

    static func jakarta16() -> UIFont? {
        return UIFont(name: "PlusJakartaSans-Regular", size: 16)
    }

    static func jakarta14() -> UIFont? {
        return UIFont(name: "PlusJakartaSans-Regular", size: 14)
    }
}
